'''
Objectif:Ceci est un module contenant la fonction destiné à calculer élever un nombre à une puissance donnée et à retourner le résultat
Méthode : on se sert de la fonction <pow> de la librairie <math>
Besoins : On a besoin de deux paramètres en entrée(x et n) que sont le nombre et sa puissance
Sortie : la fonction fournit en sortie le résultat de x^n

'''
def puissance(x,n):
  return pow(x,n)